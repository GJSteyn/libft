#include "libft.h"

int		ft_is_num(char const *s)
{
	if (*s)
	{
		if (*s == '-')
			s++;
		if (!*s)
			return (0);
		while (*s)
		{
			if (!ft_isdigit(*s))
				return (0);
			s++;
		}
		return (1);
	}
	return (1);
}
