#include "libft.h"

void		ft_insert(char **s, int index, long c)
{
	if (c < 0)
	{
		(*s)[0] = '-';
		c *= -1;
	}
	if (c > 0)
	{
		(*s)[index - 1] = (c % 10) + 48;
		c /= 10;
		index--;
		ft_insert(s, index, c);
	}
}
